public class q5 {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        String s1, s2;

        while (sc.hasNextLine()) {

            s1 = sc.next();
            s2 = sc.next();
            System.out.println(s1 + s2);
        }
    }
}
