public class fifth {
    public static void main(String[] args) {
        int x = 2;
        int y = 3;
        int z;
        // swapping
        z = x;
        x = y;
        y = z;

        System.out.println("After swapping :");
        System.out.println("Value of X is :" + x);
        System.out.println("Value of Y is :" + y);
    }
}
